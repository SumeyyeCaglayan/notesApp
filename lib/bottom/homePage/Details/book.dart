class Book {
  final String title;
  final String author;
  final String pages;
  final double rating;
  final String price;
  final bool isPaid;
  final String imageUrl;

  Book({
    required this.title,
    required this.author,
    required this.pages,
    required this.rating,
    required this.price,
    required this.isPaid,
    required this.imageUrl,
  });
}